CREATE PROCEDURE sp_UpdateXmlAndPdfId(
    @idlegalDocument INT,
    @idPdf INT,
    @idXml INT
)
AS
BEGIN

    SET LANGUAGE Spanish;
    SET NOCOUNT ON

    UPDATE LegalDocuments SET pdf= @idPdf, [xml]=@idXml WHERE id=@idlegalDocument

END

