CREATE PROCEDURE sp_GetUserPermissions(
    @userId INT
) AS 
BEGIN

    SET LANGUAGE Spanish;
    SET NOCOUNT ON

    DECLARE @isMod TINYINT

    SELECT @isMod= isPermissionMod FROM Users WHERE userID= @userId

    IF @isMod=1
        BEGIN
            SELECT id, uuid FROM UsersPermissions
        END
    ELSE
        BEGIN
            SELECT id, uuid FROM RolePermissions
        END

END