CREATE PROCEDURE sp_GetFindUser(
    @user NVARCHAR(50)
) AS 
BEGIN

    SET LANGUAGE Spanish;
    SET NOCOUNT ON
    SELECT 
        *, 
        CONCAT(firstName,' ',middleName,' ',lastName1,' ',lastName2) AS fullName
    FROM Users 
    WHERE 
        userName = @user OR 
        email = @user

END

