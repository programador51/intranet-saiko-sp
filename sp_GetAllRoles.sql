CREATE PROCEDURE sp_GetAllRoles AS 
BEGIN

    SET LANGUAGE Spanish;
    SET NOCOUNT ON
    SELECT 
        rolID AS id,
        [description] AS [name]
    FROM Roles
    WHERE [status]=1

END