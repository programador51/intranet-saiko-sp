CREATE PROCEDURE sp_DeleteDocumentItem(
    @idDocItem INT,
    @modifyBy NVARCHAR(30)
)

AS BEGIN

UPDATE DocumentItems SET
    status = 0,
    lastUpdatedBy = @modifyBy,
    lastUpdatedDate = GETDATE()

WHERE idItem = @idDocItem

END