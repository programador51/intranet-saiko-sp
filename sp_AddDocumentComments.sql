CREATE PROCEDURE sp_AddDocumentComments(
    @description NVARCHAR(200),
    @idDocument INT,
    @createdBy NVARCHAR(30)
    @order INT
)

AS BEGIN

    INSERT 

    INTO Comments

        (

            document,"order",createdBy,
            createdDate,status,description

        )

    VALUES

        (

            @idDocument, @order, @createdBy,
            GETDATE(),1,@description

        )

END
