CREATE PROCEDURE sp_UpdatePass(
	@password NVARCHAR(300),
	@userID INT,
	@modifyBy NVARCHAR (30)
) AS BEGIN
UPDATE
	Users
SET
	password = @password,
	lastUpdatedBy = @modifyBy,
	lastUpdatedDate = GETDATE()
WHERE
	userID = @userID
END